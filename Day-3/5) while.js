/* while loop=> while look is to execute block of code repeatably as long as condition true 
                condition is false this will loop will get terminated.
   while (expression) {
     Statement(s) to be executed if expression is true
   } */
//-----------------------------------------------------------------------------------------------------------------------
var a=1;
 while (a<=10){
     console.log ("hello world");
     a = a+1;    
 }
//-----------------------------------------
var a=1;
 while (a<=10){
     console.log (a+") hello world");     
     a = a+1;
     
 }

//----------------------------------------------------------------------------------------------------------------------

 //DO while loop=> the loop will always be executed at least once, even though condition is false
 var a=1;
 do{
     console.log("have nice day")
     a++;
 } while (a<=10);
 
