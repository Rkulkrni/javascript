//Assignment operators=>

//---------------------------------------------------------------------------------------------------------------------
//1) simple assignment  (a=b)

var a=12;
var b=13;

console.log(a=b);

//---------------------------------------------------------------------------------------------------------------------
//add and assignment (+= same as a=a+b)

var a=12;
var b=13;
a+=b
console.log(a);

//---------------------------------------------------------------------------------------------------------------------
//sub and assignment (-= same as a=a-b)

var a=12;
var b=13;
a-=b
console.log(a);

//---------------------------------------------------------------------------------------------------------------------
//mul and assignment (*= same as a=a*b)

var a=12;
var b=13;
a*=b
console.log(a);

//---------------------------------------------------------------------------------------------------------------------
//div and assignment (/= same as a=a/b)

var a=12;
var b=13;
a/=b;
console.log(a);

//---------------------------------------------------------------------------------------------------------------------
//modulus and assignment (%= same as a=a%b)

var a=12;
var b=5;
a%=b
console.log(a);

//---------------------------------------------------------------------------------------------------------------------
//Exponential and assignment (**= same as x=x**y)

var x=2;
var y=2;
x**=y
console.log(x);

